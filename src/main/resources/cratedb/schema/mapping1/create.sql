
CREATE TABLE IF NOT EXISTS SEMANTIC_ENTITY (
  ID string NOT NULL,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS REGION (
  ID string NOT NULL,
  FLOOR float NOT NULL,
  NAME string,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS LOCATION (
  ID string NOT NULL,
  X float NOT NULL,
  Y float NOT NULL,
  Z float NOT NULL,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS REGION_LOCATION (
  LOCATION_ID string NOT NULL,
  REGION_ID string NOT NULL,
   PRIMARY KEY(LOCATION_ID, REGION_ID)
) ;

CREATE TABLE IF NOT EXISTS INFRASTRUCTURE_TYPE (
  ID string NOT NULL,
  DESCRIPTION string ,
  NAME string,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS INFRASTRUCTURE (
  NAME string ,
  INFRASTRUCTURE_TYPE_ID string ,
  ID string NOT NULL,
  REGION_ID string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS PLATFORM_TYPE (
  ID string NOT NULL,
  DESCRIPTION string ,
  IMAGEPATH string ,
  NAME string,
  MOBILITY string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS USERS (
  EMAIL string,
  GOOGLE_AUTH_TOKEN string ,
  NAME string ,
  ID string NOT NULL,
  PRIMARY KEY (ID)
 ) ;

CREATE TABLE IF NOT EXISTS USER_GROUP (
  ID string NOT NULL,
  DESCRIPTION string ,
  NAME string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS USER_GROUP_MEMBERSHIP (
  USER_ID string NOT NULL,
  USER_GROUP_ID string NOT NULL,
  PRIMARY KEY (USER_GROUP_ID, USER_ID)
) ;

CREATE TABLE IF NOT EXISTS PLATFORM (
  ID string NOT NULL,
  DESCRIPTION string ,
  NAME string ,
  LOCATION_ID string ,
  USER_ID string ,
  PLATFORM_TYPE_ID string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS SENSOR_COVERAGE (
  ID string NOT NULL,
  RADIUS float NOT NULL,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS COVERAGE_INFRASTRUCTURE (
  ID string NOT NULL,
  INFRASTRUCTURE_ID string NOT NULL,
  PRIMARY KEY (INFRASTRUCTURE_ID, ID)
) ;

CREATE TABLE IF NOT EXISTS OBSERVATION_TYPE (
  ID string NOT NULL,
  DESCRIPTION string ,
  NAME string  ,
  PAYLOAD_SCHEMA string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS sensor_capture_functionality (
  ID string NOT NULL,
  mechanism string ,
  sensorAddress string ,
  type string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS SENSOR_TYPE (
  ID string NOT NULL,
  DESCRIPTION string ,
  IMAGE_PATH string ,
  MOBILITY string ,
  NAME string ,
  CAPTURE_FUNCTIONALITY_ID string ,
  OBSERVATION_TYPE_ID string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS SENSOR (
  ID string NOT NULL,
  DESCRIPTION string ,
  NAME string ,
  SENSOR_IP string ,
  SENSOR_PORT string ,
  COVERAGE_ID string ,
  INFRASTRUCTURE_ID string ,
  USER_ID string ,
  PLATFORM_ID string ,
  SENSOR_TYPE_ID string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS OBSERVATION (
  id string NOT NULL,
  payload string ,
  timeStamp timestamp NOT NULL,
  sensor_id string ,
  observation_type_id string ,
  PRIMARY KEY (id)
) ;

CREATE TABLE IF NOT EXISTS SEMANTIC_OBSERVATION_TYPE (
  ID string NOT NULL,
  DESCRIPTION string ,
  NAME string ,
  PAYLOAD_SCHEMA string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS VIRTUAL_SENSOR_TYPE (
  ID string NOT NULL,
  DESCRIPTION string ,
  NAME string ,
  SEMANTIC_OBSERVATION_TYPE_ID string ,
  PRIMARY KEY (id)
) ;

CREATE TABLE IF NOT EXISTS VIRTUAL_SENSOR (
  ID string NOT NULL,
  COMPLIE_COMMAND string ,
  COMPILE_DIRECTORY string ,
  COMPILED_CODE_LOCATION string ,
  DESCRIPTION string ,
  EXECUTE_COMMAND string ,
  EXECUTE_DIRECTORY string ,
  LANGUAGE string ,
  NAME string ,
  PROJECT_NAME string ,
  SOURCE_FILE_LOCATION string ,
  VS_TYPE_ID string ,
  PRIMARY KEY (ID)
) ;

CREATE TABLE IF NOT EXISTS SEMANTIC_OBSERVATION (
  ID string NOT NULL,
  CONFIDENCE float NOT NULL,
  PAYLOAD string ,
  TIMESTAMP timestamp NOT NULL,
  SEMANTIC_ENTITY_ID string ,
  SEMANTIC_OBSERVATION_TYPE_ID string ,
  VIRTUAL_SENSOR_ID string ,
  PRIMARY KEY (id)
) ;


